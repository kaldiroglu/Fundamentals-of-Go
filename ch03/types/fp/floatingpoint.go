package main

import (
	"fmt"
	"math"
)

func main() {
	fmt.Println(3.1415)
	fmt.Println(1000.0)
	fmt.Println(0.0001 + 0.0001 + 0.0001 + 0.0001 + 0.0001 + 0.0001 + 0.0001 + 0.0001 + 0.0001 + 0.0001)
	fmt.Printf("%f \n", 0.0001+0.0001+0.0001+0.0001+0.0001+0.0001+0.0001+0.0001+0.0001+0.0001)
	fmt.Printf("%v \n", 5.0/3.0)

	fmt.Printf("%f \n", 3.141592653589793238462643383279502884197169399375105820974944592307816406286)
	fmt.Printf("%v \n", 3.141592653589793238462643383279502884197169399375105820974944592307816406286)
	fmt.Printf("%v \n", 3.141_592_653_589_793_238)

	fmt.Printf("%e \n", 3.141_592_653_589_793_238)
	fmt.Printf("%v \n", 3.141_592_653_589_793_238)

	// From GoPL p.57
	for x := 0; x < 8; x++ {
		fmt.Printf("x = %d =>  e^%d = %8.3f\n", x, x, math.Exp(float64(x)))
	}

	// From GoPL p.57
	var z float64
	fmt.Println(z, -z, 1/z, -1/z, z/z)
}
